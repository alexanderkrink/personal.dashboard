/**
 * A minimal Result type for expected, recoverable failures.
 *
 * Use `Result` for domain errors the caller is expected to handle
 * (validation failures, not-found, quota exceeded). Keep `throw` for
 * programming errors and truly exceptional situations.
 */
export type Result<T, E = Error> = { ok: true; value: T } | { ok: false; error: E };

export function ok<T>(value: T): Result<T, never> {
  return { ok: true, value };
}

export function err<E>(error: E): Result<never, E> {
  return { ok: false, error };
}

/** Returns the value or throws the contained error. Use at boundaries where failure is fatal. */
export function unwrap<T, E>(result: Result<T, E>): T {
  if (result.ok) {
    return result.value;
  }
  throw result.error instanceof Error ? result.error : new Error(String(result.error));
}

export function mapResult<T, U, E>(result: Result<T, E>, fn: (value: T) => U): Result<U, E> {
  return result.ok ? ok(fn(result.value)) : result;
}
